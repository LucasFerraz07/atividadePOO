class CalculadoraSimples():
    def __init__(self):
        pass
    
    def somar(self, a, b):
        rst = a + b
        print(f"O resultado é {rst}")

    def sub(self, a, b):
        rst = a - b
        print(f"O resultado é {rst}")
    
    def dv(self, a, b):
        rst = a / b
        print(f"O resultado é {rst}")

    def mlt(self, a, b):
        rst = a * b
        print(f"O resultado é {rst}")
    
    